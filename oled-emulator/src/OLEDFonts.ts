/**
 * Helper function to create a font from raw byte data (for importing firmware fonts)
 */
export function createFontFromBytes(
  height: number,
  firstChar: number,
  widths: number[],
  data: number[],
): OLEDFont {
  return {
    height,
    firstChar,
    charCount: widths.length,
    widths,
    data: new Uint8Array(data),
  };
}
